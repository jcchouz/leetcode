class TreeNode:
    def __init__(self, x):
        self.val = x
        self.left = None
        self.right = None


#
# 代码中的类名、方法名、参数名已经指定，请勿修改，直接返回方法规定的值即可
#
#
# @param root TreeNode类
# @return bool布尔型
#
# 递归
import sys


class Solution:
    def __init__(self):
        self.pre = -sys.maxsize - 1

    def isValidBST(self, root: TreeNode) -> bool:
        # write code here
        if not root:
            return True
        if not self.isValidBST(root.left):
            return False
        if self.pre >= root.val:
            return False
        self.pre = root.val
        if not self.isValidBST(root.right):
            return False
        return True
